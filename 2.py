def schot ():
    frase = str(input('Write some words: '))

    a = frase.count('a')
    i = frase.count('i')
    u = frase.count('u')
    o = frase.count('o')
    e = frase.count('e')

    print(str(a) + ' a used times')
    print(str(i) + ' i used times')
    print(str(u) + ' u used times')
    print(str(o) + ' o used times')
    print(str(e) + ' e used times')
schot()
